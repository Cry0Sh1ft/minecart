function spawn(input) {
    switch (input) {
        case "axolotl" :
            document.write("spawned a axolotl");
        break;
        case "zombie" :
            document.write("spawned a zombie");
        break;
        case "skeleton" :
            document.write("spawned a skeleton");
        break;
    }
}
